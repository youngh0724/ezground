package com.ezground.teamproject.facilitydto;

public class FacilitySub {

	private int subNo;
	private int facilityAndFacility;
	private String subName;
	
	public int getSubNo() {
		return subNo;
	}
	public void setSubNo(int subNo) {
		this.subNo = subNo;
	}
	public int getFacilityAndFacility() {
		return facilityAndFacility;
	}
	public void setFacilityAndFacility(int facilityAndFacility) {
		this.facilityAndFacility = facilityAndFacility;
	}
	public String getSubName() {
		return subName;
	}
	public void setSubName(String subName) {
		this.subName = subName;
	}
	
	
}
